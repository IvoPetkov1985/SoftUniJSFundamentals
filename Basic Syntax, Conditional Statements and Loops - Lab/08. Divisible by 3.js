function divisibleBy3() {
    for (let i = 1; i <= 100; i++) {
        if (i % 3 === 0) {
            console.log(i);
        }
    }
}

divisibleBy3();

function divisibleByThree() {
    let number = 3;

    while (number <= 100) {
        console.log(number);
        number += 3;
    }
}

divisibleByThree();